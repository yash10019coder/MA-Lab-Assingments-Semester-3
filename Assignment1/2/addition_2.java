public class addition_2 {

    public addition_2(int a, int b) {
        System.out.println("The sum is" + (a + b));
    }

    public addition_2(int a, int b, int c) {
        System.out.println("The sum is" + (a + b + c));
    }

    public addition_2(int a, int b, int c, int d) {
        System.out.println("The sum is" + (a + b + c + d));
    }
}

